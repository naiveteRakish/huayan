package com.whayer.cloud.storage.business.base.data.msg;

import java.util.Date;

import com.whayer.cloud.storage.business.base.data.Business;

/**
 * @author fs 
 * 消息对象
 * 
 */
public class Message extends Business {

	/**
	 * 消息源对象
	 */
	private MessageSource sourceObject;

	/**
	 * 消息种类
	 */
	private MessageType msgType;
	
	/**
	 * 消息级别
	 */
	private MessageLevel msgLevel;
	
	/**
     * 消息产生特性
     */
	private MessageGenFeature msgGenerateType;
	
	/**
	 * 消息产生时间
	 */
	private Date msgTime;

	/**
	 * 消息附加内容
	 */
	private MessageBody msgBody;

	/**
	 * 获取
	 * @return sourceObject
	 */
	public MessageSource getSourceObject() {
		return sourceObject;
	}

	/**
	 * 设置
	 * @param sourceObject
	 */
	public void setSourceObject(MessageSource sourceObject) {
		this.sourceObject = sourceObject;
	}

	/**
	 * 获取
	 * @return msgType
	 */
	public MessageType getMsgType() {
		return msgType;
	}

	/**
	 * 设置
	 * @param msgType
	 */
	public void setMsgType(MessageType msgType) {
		this.msgType = msgType;
	}

	/**
	 * 获取
	 * @return msgLevel
	 */
	public MessageLevel getMsgLevel() {
		return msgLevel;
	}

	/**
	 * 设置
	 * @param msgLevel
	 */
	public void setMsgLevel(MessageLevel msgLevel) {
		this.msgLevel = msgLevel;
	}

	/**
	 * 获取
	 * @return msgGenerateType
	 */
	public MessageGenFeature getMsgGenerateType() {
		return msgGenerateType;
	}

	/**
	 * 设置
	 * @param msgGenerateType
	 */
	public void setMsgGenerateType(MessageGenFeature msgGenerateType) {
		this.msgGenerateType = msgGenerateType;
	}

	/**
	 * 获取
	 * @return msgTime
	 */
	public Date getMsgTime() {
		return msgTime;
	}

	/**
	 * 设置
	 * @param msgTime
	 */
	public void setMsgTime(Date msgTime) {
		this.msgTime = msgTime;
	}

	/**
	 * 获取
	 * @return msgBody
	 */
	public MessageBody getMsgBody() {
		return msgBody;
	}

	/**
	 * 设置
	 * @param msgBody
	 */
	public void setMsgBody(MessageBody msgBody) {
		this.msgBody = msgBody;
	}

	
	
}
