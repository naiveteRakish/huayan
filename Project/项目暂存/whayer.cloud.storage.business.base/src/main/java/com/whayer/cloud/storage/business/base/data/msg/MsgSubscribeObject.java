package com.whayer.cloud.storage.business.base.data.msg;

import java.util.Date;


/**
 * @author fs
 * 消息订阅对象
 * 
 */
public class MsgSubscribeObject {
	/**
	 * 消息源对象
	 */
	private MessageSource sourceObject;
	/**
	 * 消息种类
	 */
	private MessageType msgType;
	/**
	 * 订阅消息开始级别
	 */
	private MessageLevel msgBeginLevel;
	/**
	 * 订阅消息结束级别
	 */
	private MessageLevel msgEndLevel;
	/**
     * 有效订阅开始时间（未填写表明从此刻开始有效，直到validEndTime）
     */
	private Date validBeginTime;
	/**
     * 有效订阅结束时间（未填写表明从validBeginTime开始一直有效）
     */
	private Date validEndTime;
	/**
	 * 获取
	 * @return sourceObject
	 */
	public MessageSource getSourceObject() {
		return sourceObject;
	}
	/**
	 * 设置
	 * @param sourceObject
	 */
	public void setSourceObject(MessageSource sourceObject) {
		this.sourceObject = sourceObject;
	}
	/**
	 * 获取
	 * @return msgType
	 */
	public MessageType getMsgType() {
		return msgType;
	}
	/**
	 * 设置
	 * @param msgType
	 */
	public void setMsgType(MessageType msgType) {
		this.msgType = msgType;
	}
	
	/**
	 * @return the msgBeginLevel
	 */
	public MessageLevel getMsgBeginLevel() {
		return msgBeginLevel;
	}
	/**
	 * @param msgBeginLevel the msgBeginLevel to set
	 */
	public void setMsgBeginLevel(MessageLevel msgBeginLevel) {
		this.msgBeginLevel = msgBeginLevel;
	}
	/**
	 * @return the msgEndLevel
	 */
	public MessageLevel getMsgEndLevel() {
		return msgEndLevel;
	}
	/**
	 * @param msgEndLevel the msgEndLevel to set
	 */
	public void setMsgEndLevel(MessageLevel msgEndLevel) {
		this.msgEndLevel = msgEndLevel;
	}
	/**
	 * 获取
	 * @return validBeginTime
	 */
	public Date getValidBeginTime() {
		return validBeginTime;
	}
	/**
	 * 设置
	 * @param validBeginTime
	 */
	public void setValidBeginTime(Date validBeginTime) {
		this.validBeginTime = validBeginTime;
	}
	/**
	 * 获取
	 * @return validEndTime
	 */
	public Date getValidEndTime() {
		return validEndTime;
	}
	/**
	 * 设置
	 * @param validEndTime
	 */
	public void setValidEndTime(Date validEndTime) {
		this.validEndTime = validEndTime;
	}
	
	
}
