package com.whayer.cloud.storage.foundation.filemarkaccess.beans;


/**
 * 图片文件描述对象
 * @ClassName: ImageFileInfoPojo
 * @author fsong
 * @date 2017年8月24日 下午5:02:48
 * @version v1.0.0
 * 
 */
public class ImageFileInfoPojo extends BaseFileInfoPojo{
	
	/**
	 * 自定义备注信息（选填）
	 * @Fields markDesc
	 */
	private String markDesc;
	
	/**
	 * 图片宽（选填 ：-1表示未知）
	 * @Fields width
	 */
	private int width;
	
	/**
	 * 图片高（选填 ：-1表示未知）
	 * @Fields height
	 */
	private int height;
	
	/**
	 * 图片格式 (选填，取值ImageType枚举）
	 * @Fields imageType
	 */
	private int imageType;

	/**
	 * @return the markDesc
	 */
	public String getMarkDesc() {
		return markDesc;
	}

	/**
	 * @param markDesc the markDesc to set
	 */
	public void setMarkDesc(String markDesc) {
		this.markDesc = markDesc;
	}

	/**
	 * @return the width
	 */
	public int getWidth() {
		return width;
	}

	/**
	 * @param width the width to set
	 */
	public void setWidth(int width) {
		this.width = width;
	}

	/**
	 * @return the height
	 */
	public int getHeight() {
		return height;
	}

	/**
	 * @param height the height to set
	 */
	public void setHeight(int height) {
		this.height = height;
	}

	/**
	 * @return the imageType
	 */
	public int getImageType() {
		return imageType;
	}

	/**
	 * @param imageType the imageType to set
	 */
	public void setImageType(int imageType) {
		this.imageType = imageType;
	}		
	
}
